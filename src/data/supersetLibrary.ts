export function getSupersets(
  goal: Goal,
  equipment: Equipment,
  experience: Experience
): Superset[] {
  return supersets[goal][equipment][experience];
}

export function getRandomSuperset(
  goal: Goal,
  equipment: Equipment,
  experience: Experience
): Superset | null {
  const list = getSupersets(goal, equipment, experience);
  if (!list.length) return null;
  return list[Math.floor(Math.random() * list.length)];
}

export function getRandomSupersets(
  goal: Goal,
  equipment: Equipment,
  experience: Experience,
  count: number
): Superset[] {
  const list = [...getSupersets(goal, equipment, experience)];
  for (let i = list.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [list[i], list[j]] = [list[j], list[i]];
  }
  return list.slice(0, Math.min(count, list.length));
}
