// supersets.ts
// App-ready superset library
// Structure: goal -> equipment -> experience -> list of supersets
// Notes:
// - Heavy compound lifts are intentionally excluded from supersets
// - Curated for accessory/intense but generally safer pairing logic
// - You can randomize within any filtered bucket

export type Goal = "strength" | "muscle" | "athletic";
export type Equipment = "gym" | "dumbbells" | "home";
export type Experience = "beginner" | "intermediate";

export type SupersetType =
  | "same_muscle"
  | "push_pull"
  | "upper_lower"
  | "posterior_chain"
  | "arms"
  | "shoulders"
  | "legs"
  | "core"
  | "conditioning"
  | "power_conditioning"
  | "unilateral_stability";

export interface SupersetExercise {
  name: string;
  reps: string;
}

export interface Superset {
  id: string;
  goal: Goal;
  equipment: Equipment;
  experience: Experience;
  type: SupersetType;
  difficulty: 1 | 2 | 3;
  exercises: [SupersetExercise, SupersetExercise];
  restSeconds: number;
  notes?: string[];
}

const makeId = (
  goal: Goal,
  equipment: Equipment,
  experience: Experience,
  index: number
) => `${goal}_${equipment}_${experience}_${String(index).padStart(3, "0")}`;

